      <p><em>Chicagoans suggest tornado siren, sound of rats<br>
        <br>
      </em>What is your favorite Chicago sound?<br>
      <br>
      Jesse Seay wants to know -- actually, she needs to know, because that's what her nascent Web site (favoritechicagosounds.com) is all about. Participation is simply a matter of logging on and typing in the details of your fondest aural experiences.<br>
      <br>
      The list makes entertaining, evocative reading -- &quot;The tornado/ curfew siren while watching the 10 o'clock news on a summer night. Always felt good as a kid to make it home before the siren went off at 10:30. Reminds me of being young&quot;; &quot;Birds swimming (fountains in the park). They're so sweet-sounding&quot; -- even if it is a bit heavy on paeans to the L.<br>
      <br>
      &quot;The more specific your description the better,&quot; says Seay, especially because where and when a sound occurs helps her track it down and record it. And she wants details about the memories and/or emotions your sounds evoke.<br>
      <br>
      One person suggested the sound of rats crawling between the walls of a certain bookstore. &quot;I love his submission. It's unusual, and it's so secret -- you would only notice it if you were standing in a very specific spot ('with your back to the Richard Powers books') right when a train passed. This is part of what I love about this project -- finding the secret sounds of the city that mean so much to its residents.&quot;<br>
      <br>
      Seay is looking for volunteers. In fact, most of the sounds on the site so far have come from volunteers and from WBEZ. If you want to get into the field-recording business, you can submit MP3s by clicking &quot;upload a recording&quot; next to whichever sound suggestion you have chosen to record, or your own suggestion.<br>
      <br>
      Seay has this advice for amateur recorders:<br>
      <br>
      - &quot;Know your equipment. Know how to turn it on and off without looking at it. Know what is too loud or too soft for your equipment to handle, and know how to adjust the recording levels quickly. All of this comes with practice.&quot;<br>
      <br>
      - &quot;Keep the recorder running. The best sound always happens the second after you hit 'stop.'&quot;<br>
      <br>
      - &quot;Don't talk.&quot;<br>
      <br>
      - &quot;Ambient sounds can be very beautiful. Sonic 'events' can also be interestingthe sound of something happening. At the moment I'm pursuing a good recording in the Merc. I have recorded some decent ambient sound, but what I'd really like. . . is the sound of the bidding right before the closing bell shuts the floor down for the day. That's an event.&quot;<br>
      <br>
      The site is still a work-in-progress. &quot;We are going to organize it by neighborhoods. ... So we're eventually going to have a map that will point to where all the sounds are,&quot; says Seay. In the meantime, there's the jukebox of playable sounds, including one of Seay's favorites: the wolves at the Lincoln Park Zoo howling at ambulance sirens. &quot;It's a beautiful, weird chorus, and I love how their voices are spread out along the length of the wolves' enclosure -- singing in stereo!&quot;<br>
      <br>
      --Emily Nunn</p>
    
