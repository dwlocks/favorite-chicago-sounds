    <td width="700" rowspan="3" align="left" valign="top" class="bodycopy"> <br><p><a href="press.php">New City review - Train Time: A Sound Installation</a> | 11.5.08 <br>
      <a href="press_timeoutchicago.php">Time Out Chicago feature </a> | 2.7.08 <br>
        <a href="press_pedfactory.php">Chicago Reader review - Pedagogical Factory</a> | 9.7.07 
      <br><a href="press_tribune.php">Chicago Tribune feature</a> | 12.7.06

		<a href="http://www.chicagotribune.com/features/lifestyle/living/chi-chicagosounds-flashhtml,1,479958.htmlstory?track=sto-relcon" target="_new"><br>
		Multimedia slide show accompanying Chicago Tribune feature</a> | 12.7.06
          </p>
    <img src="images/press_tribune.jpg" width="300" height="200" hspace="10" vspace="7" align="right"><span class="headline1"><em>She's listening</em><br>
        </span>[Transcript from 
        Chicago Tribune]<br>
        Author: Emily Nunn, Tribune staff reporter<br>
        Date: Dec 7, 2006<br>
        Section: Tempo<br>
        <br>
        JESSE SEAY IS ONE OF THE MOST FANTASTIC LISTENERS YOU'LL EVER MEET.<br>
        <br>
        But you might not notice it because she has a tendency to wear headphones when you're talking to her, and sometimes, as you're blabbing away, she might close her eyes, as if she has gone to sleep.<br>
        <br>
        Still, her attentions are acute. Seay is the woman behind Your Favorite Chicago Sounds (favoritechicagosounds.com ), an online public archive that aims to reflect the aural beauty (and the bedlam) of our city.<br>
        <br>
        Thanks to her, in the year 2525, if man is still alive, we'll still be able to hear the unintentionally wry and poignant &quot;L&quot; announcement, &quot;This is Grand.&quot;<br>
        <br>
        Not to mention the eerie sound of the wolves at Lincoln Park Zoo howling at ambulances at night, the roar of Wrigley Field on game day, the clamor of Union Station, chunks of ice rubbing together on the lake in winter and those crazy parakeets squawking away in the trees in Hyde Park.<br>
        <br>
        Just to name few.<br>
        <br>
        Seay, 31 -- a media artist who teaches at Columbia College Chicago and has a solo show of her aural art opening in April (&quot;I've been interested in stainless steel balls, pachinko balls . . . and I also want to do something involving Ping-Pong balls,&quot; she says) -- was entranced by the way the world sounds from a very early age.<br>
        <br>
&quot;I think I've always paid a lot more attention to [my ears] than I have my eyes,&quot; said Seay, who, like a lot of people who do sound art and field recording, is also a musician.<br>
        <br>
&quot;I play the flute and instruments that don't really qualify as instruments. . . . I've had a lot of success with a turkey caller. I got it out of a hunting catalog.&quot;<br>
        <br>
        The bottom line: &quot;I'd go nuts if I couldn't hear,&quot; she said on a recent afternoon, as we headed out to gather some sound. She was decked out in her recording/listening get-up, a special belt she made herself (from rappelling equipment) holding her powerful minidisc recorder, attached to her headphones, which are actually not headphones but powerful binaural microphones that have the advantage of not only recording sound at a naturalistic ear level but also give her stealthiness. &quot;People don't know what they are,&quot; she said of the headphone/microphones.<br>
        <br>
        YFCS is following in the footsteps of the British musician, sound artist and educator Peter Cusack, 58, whose similar but earlier &quot;field-recording&quot; work culminated in the CD &quot;Your Favourite London Sounds&quot; and a CD to be released next year, &quot;Favourite Sounds of Beijing,&quot; and who firmly believes that one man's noise is another man's music. (&quot;You will find people who actually like traffic noise . . . a lot of the sounds that you hear complaints about -- you can always find somebody who likes it,&quot; he says.) He helped get Seay started on the project this spring when he was in town to teach, and she was still completing her master of fine arts degree in the film video and new media program at the School of the Art Institute of Chicago.<br>
        <br>
        But while Cusack's projects are somewhat old-fashioned by today's standards, Seay's reflects her Internet-egalitarian, podcasting generation: It is open-ended, and anyone can contribute favorite sounds and recordings to the site.<br>
        <br>
        <em>A growing archive<br>
        <br>
        </em>&quot;There will be no final product -- the archive will just continue to grow; the Web site will just be an online resource that we'll continue to access,&quot; she said. &quot;We're not ever going to close the doors. But eventually, maybe, when I die . . . the project will die with me.&quot;<br>
        <br>
        Another difference, however, is that Cusack's old-fashioned CD for the London project sold well enough to pay for the project, but Seay is not getting paid, and she'll rely on volunteers for getting the sounds then transferring them to the site's jukebox. (However, the project is under the auspices of the non-profit Experimental Sound Studio, in Ravenswood, and &quot;we have a cross promotion with WBEZ and their Sonic Soundscape series -- we can use their sounds and they can use ours.&quot;)<br>
        <br>
        Now that she's teaching full time, she does field recording when she can, and she compares the process to a scavenger hunt or a fishing expedition. Sometimes you go looking for one thing but find something else entirely -- or come up empty.<br>        
        <br>
        The Shedd Aquarium, for example. It seemed like a fantastic place to capture sound. Right? The soothing splashing of dolphins returning to the water, the lyrical whistling and clicking of the whales, the frenzied honking of the penguins?<br>
        <br>
        But the Shedd was so chaotic--a cacophony of prerecorded sound (the whales, for instance), not to mention music, people talking and rushing water--that it drowned out the dolphins' splashes.<br>
        <br>
&quot;The kind of sound I like is natural, and is easy to isolate,&quot; she said, although she is not opposed to ambient sounds and such unexpected juxtapositions as the wolves and ambulance combo. &quot;I think, though, that it's helpful to find environments where the sounds you're interested can be heard clearly, where white noise is not covering it up. This is a very live space,&quot; she said kindly.<br>
        <br>
        But it wasn't a total loss: On the way out, a man in full scuba gear was suspended inside the round Caribbean reef tank in the Rotunda, shaking green peas from a plastic container to the fish and turtles and shark that swam around him, his white hair floating around his head like King Neptune. He was talking to the crowd by microphone, over the sound of his oxygen tank, about whether or not the fish make any noise. &quot;You can sometimes hear the snapping sound when his jaw closes,&quot; the man said of the barracuda, before taking in a long hissing gasp of air. &quot;He has a very large mouth, and his jaw drops down and sort of snaps.&quot;<br>
        <br>
        Seay implied she might be able to use some of that, before we hopped into a cab and headed to the Billy Goat, where she hoped to record the famous &quot;cheesborger&quot; routine. But on this day, the guys behind the counter were almost passive-aggressively silent, no matter how much we baited them (&quot;I think we'll have two cheeseburgers.&quot;).<br>
        <br>
        <em>Touring downtown<br>
        <br>
        </em>Seay seemed undeterred (&quot;I'll come back,&quot; she said). Next, it was down to Wacker and Michigan (crossing the bridge reminded her that she wanted to come back and record the drawbridge bell) to find the street saxophonist who plays nothing but the theme song from &quot;Sanford and Son,&quot; over and over again, which seemed to qualify as both music and noise. It was very cold, so he was absent, and we continued south to the northeast corner of Wabash and Randolph, to hear one of my personal favorite Chicago sounds: people getting off the &quot;L,&quot; overhead, at the end of the day.<br>
        <br>
        I felt a bit worried after the Shedd fiasco. A train roared into the station above our heads, then came to a halt. People yelled and laughed, car horns honked, construction workers clanged a block away and the train pulled out again, taking its screech and roar with it.<br>
        <br>
        Then there it was, the lovely, almost mellow clomp-clomp- clomping of people walking along the platform over our heads, then descending the wooden steps.<br>
        <br>
        Seay stood beneath the platform, closed her eyes and listened for a long time, as trains arrived and departed, arrived and departed, oblivious to the crowds of workers all around her, like someone listening to the most beautiful concert ever.<br>
        <br>
        ----------<br>
        <br>
        ernunn@tribune.com</p>
