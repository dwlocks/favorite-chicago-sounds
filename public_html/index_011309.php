             <p><span class="headline1">(What is FCS?)<br />
</span>

				<span class="bodycopy">
				It works like
this: we ask local residents to name their favorite sounds of the city. We
then seek out those  sounds and make field recordings, which are
available to the public as MP3s on our web site.<br>
<br>
Please join us in developing this acoustic archive of the city. 
<a href="participate.php">Fill out our survey</a> or 
<a href="catalog.php">visit our  catalog</a> to contribute 
your own sounds. If we record your sound, we'll let you know, 
and you'll be able to listen to it or any of a growing number 
of Chicago's Favorite Sounds anytime you like.<br>
<br>
Feel free to download, listen, and produce new work from our <a href="recordings.php">collection of recordings</a>. The files are free to use. We only ask that you acknowledge us in your work. Please send us a copy of what you create&ndash;we'd love to post it on the site (with your permission) and show off your creativity!<br>
