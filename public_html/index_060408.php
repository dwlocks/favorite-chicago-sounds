            <p><span class="headline1">What is FCS?<br /></span>

			<span class="headline2">
				<span class="bodycopy"> Favorite Chicago Sounds is the Chicago incarnation of an international project started by sound artist <a href="http://www.favouritelondonsounds.org/" target="_blank">Peter Cusack</a> in 1998.  <br>
				<br>
				The project works like
this: we ask local residents to name their favorite sounds of the city. We
then seek out those favorite sounds and make field recordings, which are
available to the public as MP3s on our web site.<br>
<br>
Please join us in developing this acoustic archive of the city. 
<a href="participate.php">Fill out our survey</a> or 
<a href="catalog.php">visit our catalog</a> to contribute 
your own sounds. If we record your sound, we'll let you know, 
and you'll be able to listen to it or any of a growing number 
of Chicago's Favorite Sounds anytime you like.</span><br>
<br /><span class="bodycopy">
<a href="http://www.exsost.org/" target="_blank"><img src="images/ess_logo_blu.gif" width="170" height="26" hspace="5" vspace="3" border="0" align="right"></a>FCS is a program of <a href="http://www.exsost.org/" target="_blank">Experimental Sound Studio</a>, a nonprofit sonic arts organization in Chicago.<br>
                    </span><br>
                    <br>
<span class="headline1">Get Involved </span><br>
